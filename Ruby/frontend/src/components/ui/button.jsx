import { cva } from 'class-variance-authority';
import { cn } from '../../lib/utils';

const buttonVariants = cva(
  'inline-flex items-center justify-center rounded-md text-sm font-semibold transition focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-60',
  {
    variants: {
      variant: {
        default: 'bg-gradient-to-r from-indigo-500 via-purple-500 to-pink-500 text-white shadow-lg shadow-indigo-500/30 hover:brightness-110 focus-visible:ring-indigo-400',
        secondary: 'bg-white/10 border border-white/20 text-white hover:bg-white/20 focus-visible:ring-white/40',
        outline: 'border border-white/30 text-white hover:bg-white/10 focus-visible:ring-white/30',
        destructive: 'bg-red-500 text-white hover:bg-red-600 focus-visible:ring-red-400',
        ghost: 'text-white hover:bg-white/10 focus-visible:ring-white/30',
      },
      size: {
        default: 'h-11 px-5',
        sm: 'h-9 px-4 text-xs',
        lg: 'h-12 px-6 text-base',
        icon: 'h-10 w-10',
      },
    },
    defaultVariants: {
      variant: 'default',
      size: 'default',
    },
  },
);

export function Button({ className, variant, size, ...props }) {
  return (
    <button className={cn(buttonVariants({ variant, size }), className)} {...props} />
  );
}
